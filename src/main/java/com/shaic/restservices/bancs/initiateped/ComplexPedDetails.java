package com.shaic.restservices.bancs.initiateped;

public class ComplexPedDetails {
	
	String pedGroupCode;
	String pedICDChapter;
	String pedICDBlock;
	String pedICDCode;
	String pedDescription;
	
	public String getPedGroupCode() {
		return pedGroupCode;
	}
	public void setPedGroupCode(String pedGroupCode) {
		this.pedGroupCode = pedGroupCode;
	}
	public String getPedICDChapter() {
		return pedICDChapter;
	}
	public void setPedICDChapter(String pedICDChapter) {
		this.pedICDChapter = pedICDChapter;
	}
	public String getPedICDBlock() {
		return pedICDBlock;
	}
	public void setPedICDBlock(String pedICDBlock) {
		this.pedICDBlock = pedICDBlock;
	}
	public String getPedICDCode() {
		return pedICDCode;
	}
	public void setPedICDCode(String pedICDCode) {
		this.pedICDCode = pedICDCode;
	}
	public String getPedDescription() {
		return pedDescription;
	}
	public void setPedDescription(String pedDescription) {
		this.pedDescription = pedDescription;
	}
	
	

}
