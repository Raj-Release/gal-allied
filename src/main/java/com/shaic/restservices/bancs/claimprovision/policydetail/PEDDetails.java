package com.shaic.restservices.bancs.claimprovision.policydetail;

public class PEDDetails {
	
	private Integer serialNo;
	private String pedSource;
	private String pedGroupCode;
	private String pedICDChapter;
	private String pedICDBlock;
	private String pedICDCode;
	private String pedDescription;
	private String policyWording;
	
	public Integer getSerialNo() {
		return serialNo;
	}
	public void setSerialNo(Integer serialNo) {
		this.serialNo = serialNo;
	}
	public String getPedSource() {
		return pedSource;
	}
	public void setPedSource(String pedSource) {
		this.pedSource = pedSource;
	}
	public String getPedGroupCode() {
		return pedGroupCode;
	}
	public void setPedGroupCode(String pedGroupCode) {
		this.pedGroupCode = pedGroupCode;
	}
	public String getPedICDChapter() {
		return pedICDChapter;
	}
	public void setPedICDChapter(String pedICDChapter) {
		this.pedICDChapter = pedICDChapter;
	}
	public String getPedICDBlock() {
		return pedICDBlock;
	}
	public void setPedICDBlock(String pedICDBlock) {
		this.pedICDBlock = pedICDBlock;
	}
	public String getPedICDCode() {
		return pedICDCode;
	}
	public void setPedICDCode(String pedICDCode) {
		this.pedICDCode = pedICDCode;
	}
	public String getPedDescription() {
		return pedDescription;
	}
	public void setPedDescription(String pedDescription) {
		this.pedDescription = pedDescription;
	}
	public String getPolicyWording() {
		return policyWording;
	}
	public void setPolicyWording(String policyWording) {
		this.policyWording = policyWording;
	}
	
}
