package com.shaic.restservices.bancs.claimprovision.policydetail;

public class InceptionDateWiseDistributionDetails {
	
	private Integer serialNo;
	private String riskInceptionDate;
	private Integer sumInsuredIndividual;
	private Integer sumInsuredFloaterBasis;
	private Integer cumulativeBonusIndividual;
	private Integer cumulativeBonusFloater;
	
	public Integer getSerialNo() {
		return serialNo;
	}
	public void setSerialNo(Integer serialNo) {
		this.serialNo = serialNo;
	}
	public String getRiskInceptionDate() {
		return riskInceptionDate;
	}
	public void setRiskInceptionDate(String riskInceptionDate) {
		this.riskInceptionDate = riskInceptionDate;
	}
	public Integer getSumInsuredIndividual() {
		return sumInsuredIndividual;
	}
	public void setSumInsuredIndividual(Integer sumInsuredIndividual) {
		this.sumInsuredIndividual = sumInsuredIndividual;
	}
	public Integer getSumInsuredFloaterBasis() {
		return sumInsuredFloaterBasis;
	}
	public void setSumInsuredFloaterBasis(Integer sumInsuredFloaterBasis) {
		this.sumInsuredFloaterBasis = sumInsuredFloaterBasis;
	}
	public Integer getCumulativeBonusIndividual() {
		return cumulativeBonusIndividual;
	}
	public void setCumulativeBonusIndividual(Integer cumulativeBonusIndividual) {
		this.cumulativeBonusIndividual = cumulativeBonusIndividual;
	}
	public Integer getCumulativeBonusFloater() {
		return cumulativeBonusFloater;
	}
	public void setCumulativeBonusFloater(Integer cumulativeBonusFloater) {
		this.cumulativeBonusFloater = cumulativeBonusFloater;
	}
	
}
