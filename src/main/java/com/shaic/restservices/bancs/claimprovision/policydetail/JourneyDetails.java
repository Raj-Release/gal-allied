package com.shaic.restservices.bancs.claimprovision.policydetail;

public class JourneyDetails {
	
	private Integer serialNo;
	private String dateOfJourney;
	private Integer noOfTravelDays;
	private String endDateOfJourney;
	private String placeOfTravel;
	private String visaType;
	private String purposeOfVisit;
	private String currencyType;
	private String certificateNumber;
	
	public Integer getSerialNo() {
		return serialNo;
	}
	public void setSerialNo(Integer serialNo) {
		this.serialNo = serialNo;
	}
	public String getDateOfJourney() {
		return dateOfJourney;
	}
	public void setDateOfJourney(String dateOfJourney) {
		this.dateOfJourney = dateOfJourney;
	}
	public Integer getNoOfTravelDays() {
		return noOfTravelDays;
	}
	public void setNoOfTravelDays(Integer noOfTravelDays) {
		this.noOfTravelDays = noOfTravelDays;
	}
	public String getEndDateOfJourney() {
		return endDateOfJourney;
	}
	public void setEndDateOfJourney(String endDateOfJourney) {
		this.endDateOfJourney = endDateOfJourney;
	}
	public String getPlaceOfTravel() {
		return placeOfTravel;
	}
	public void setPlaceOfTravel(String placeOfTravel) {
		this.placeOfTravel = placeOfTravel;
	}
	public String getVisaType() {
		return visaType;
	}
	public void setVisaType(String visaType) {
		this.visaType = visaType;
	}
	public String getPurposeOfVisit() {
		return purposeOfVisit;
	}
	public void setPurposeOfVisit(String purposeOfVisit) {
		this.purposeOfVisit = purposeOfVisit;
	}
	public String getCurrencyType() {
		return currencyType;
	}
	public void setCurrencyType(String currencyType) {
		this.currencyType = currencyType;
	}
	public String getCertificateNumber() {
		return certificateNumber;
	}
	public void setCertificateNumber(String certificateNumber) {
		this.certificateNumber = certificateNumber;
	}
	
}
