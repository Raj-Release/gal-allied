package com.shaic.restservices.bancs.claimprovision.policydetail;

public class ServiceTaxDetails {

	private Integer serialNumber;
	private String serviceTaxApplicable;
	private String serviceTaxCode;
	private String serviceTaxAssesseeCode;
	private String serviceTaxRegNumber;
	private String effectiveFrom;
	private String effectiveTo;
	
	public Integer getSerialNumber() {
		return serialNumber;
	}
	public void setSerialNumber(Integer serialNumber) {
		this.serialNumber = serialNumber;
	}
	public String getServiceTaxApplicable() {
		return serviceTaxApplicable;
	}
	public void setServiceTaxApplicable(String serviceTaxApplicable) {
		this.serviceTaxApplicable = serviceTaxApplicable;
	}
	public String getServiceTaxCode() {
		return serviceTaxCode;
	}
	public void setServiceTaxCode(String serviceTaxCode) {
		this.serviceTaxCode = serviceTaxCode;
	}
	public String getServiceTaxAssesseeCode() {
		return serviceTaxAssesseeCode;
	}
	public void setServiceTaxAssesseeCode(String serviceTaxAssesseeCode) {
		this.serviceTaxAssesseeCode = serviceTaxAssesseeCode;
	}
	public String getServiceTaxRegNumber() {
		return serviceTaxRegNumber;
	}
	public void setServiceTaxRegNumber(String serviceTaxRegNumber) {
		this.serviceTaxRegNumber = serviceTaxRegNumber;
	}
	public String getEffectiveFrom() {
		return effectiveFrom;
	}
	public void setEffectiveFrom(String effectiveFrom) {
		this.effectiveFrom = effectiveFrom;
	}
	public String getEffectiveTo() {
		return effectiveTo;
	}
	public void setEffectiveTo(String effectiveTo) {
		this.effectiveTo = effectiveTo;
	}
	
}
